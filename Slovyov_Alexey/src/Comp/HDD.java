public class HDD {
    private String brendH;
    private int sizeH;

    public HDD() {
    }

    public HDD(String brendH, int sizeH) {
        this.brendH = brendH;
        this.sizeH = sizeH;
    }

    public String getBrendH() {
        return brendH;
    }

    public void setBrendH(String brendH) {
        this.brendH = brendH;
    }

    public int getSizeH() {
        return sizeH;
    }

    public void setSizeH(int sizeH) {
        this.sizeH = sizeH;
    }
}
