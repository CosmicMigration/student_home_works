public class KeyBoard {
    private String brendK;
    private String colour;

    public KeyBoard() {
    }

    public KeyBoard(String brendK, String colour) {
        this.brendK = brendK;
        this.colour = colour;
    }

    public String getBrendK() {
        return brendK;
    }

    public void setBrendK(String brendK) {
        this.brendK = brendK;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }
}
