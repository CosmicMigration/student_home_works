package magaz.sport_goods;

public class Info {

    private String adr;
    private String email;
    private int postCod;

    public String getAdr() {
        return adr;
    }

    public void setAdr(String adr) {
        this.adr = adr;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getPostCod() {
        return postCod;
    }

    public void setPostCod(int postCod) {
        this.postCod = postCod;
    }
}
